export const getBlogByIdOrSlug = (idOrSlug) => {
  const normalized = String(idOrSlug || "").trim().toLowerCase();
  return (
    blogCatalog.find(
      (blog) =>
        String(blog.id) === normalized || String(blog.slug).toLowerCase() === normalized
    ) || null
  );
};

export const getRecentBlogs = (limit = 5) => blogCatalog.slice(0, limit);

export const getRelatedBlogs = (currentBlog, limit = 3) => {
  if (!currentBlog) return blogCatalog.slice(0, limit);
  const sameCategory = blogCatalog.filter(
    (blog) => blog.id !== currentBlog.id && blog.category === currentBlog.category
  );
  const fallback = blogCatalog.filter((blog) => blog.id !== currentBlog.id);
  const unique = [];
  const seen = new Set();

  [...sameCategory, ...fallback].forEach((blog) => {
    if (!seen.has(blog.id)) {
      seen.add(blog.id);
      unique.push(blog);
    }
  });

  return unique.slice(0, limit);
};

export const getBlogCategoryCounts = () =>
  blogCategories.reduce((acc, category) => {
    acc[category] = blogCatalog.filter((blog) => blog.category === category).length;
    return acc;
  }, {});

export const getBlogNeighbors = (currentBlog) => {
  const index = blogCatalog.findIndex((blog) => blog.id === currentBlog?.id);
  if (index < 0) return { previous: null, next: null };
  return {
    previous: blogCatalog[index - 1] || null,
    next: blogCatalog[index + 1] || null,
  };
};

export const normalizeBlogCategory = (category) => {
  if (!category || String(category).trim().toLowerCase() === "all") return "All";
  const normalized = String(category).trim().toLowerCase();
  const found = blogCategories.find((item) => item.toLowerCase() === normalized);
  return found || "All";
};

export const getBlogListingResult = ({
  category = "All",
  query = "",
  page = 1,
  limit = 9,
} = {}) => {
  const activeCategory = normalizeBlogCategory(category);
  const normalizedQuery = String(query || "").trim().toLowerCase();
  const safeLimit = Math.max(1, Number(limit) || 9);

  let filtered = blogCatalog;

  if (activeCategory !== "All") {
    filtered = filtered.filter((blog) => blog.category === activeCategory);
  }

  if (normalizedQuery) {
    filtered = filtered.filter((blog) => {
      const haystack = [
        blog.title,
        blog.excerpt,
        blog.category,
        blog.author,
        Array.isArray(blog.content)
          ? blog.content
              .map((block) => block?.text || block?.html || "")
              .join(" ")
          : "",
      ]
        .join(" ")
        .toLowerCase();
      return haystack.includes(normalizedQuery);
    });
  }

  const totalItems = filtered.length;
  const totalPages = Math.max(1, Math.ceil(totalItems / safeLimit));
  const pageNumber = Math.max(1, Number(page) || 1);
  const currentPage = Math.min(pageNumber, totalPages);
  const start = (currentPage - 1) * safeLimit;
  const items = filtered.slice(start, start + safeLimit);

  return {
    items,
    totalItems,
    totalPages,
    currentPage,
    activeCategory,
    query: normalizedQuery,
    limit: safeLimit,
  };
};
